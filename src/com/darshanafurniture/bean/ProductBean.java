package com.darshanafurniture.bean;


public class ProductBean {

	private long productId;
	private String productName;
	private double buyingPrice;
	private double transportCost;
	private double productionCost;
	private double finishingCost;
	private double otherEssentialCost;
	private double totalProductCostWithoutMaintainingCost;
	private double lightAndMaintainCost;
	private double totalProductCost;
	private double productLablingPrice;
	
	public long getProductId() {
		return productId;
	}
	public void setProductId(long productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public double getBuyingPrice() {
		return buyingPrice;
	}
	public void setBuyingPrice(double buyingPrice) {
		this.buyingPrice = buyingPrice;
	}
	public double getTransportCost() {
		return transportCost;
	}
	public void setTransportCost(double transportCost) {
		this.transportCost = transportCost;
	}
	public double getProductionCost() {
		return productionCost;
	}
	public void setProductionCost(double productionCost) {
		this.productionCost = productionCost;
	}
	public double getFinishingCost() {
		return finishingCost;
	}
	public void setFinishingCost(double finishingCost) {
		this.finishingCost = finishingCost;
	}
	public double getOtherEssentialCost() {
		return otherEssentialCost;
	}
	public void setOtherEssentialCost(double otherEssentialCost) {
		this.otherEssentialCost = otherEssentialCost;
	}
	public double getTotalProductCostWithoutMaintainingCost() {
		return totalProductCostWithoutMaintainingCost;
	}
	public void setTotalProductCostWithoutMaintainingCost(
			double totalProductCostWithoutMaintainingCost) {
		this.totalProductCostWithoutMaintainingCost = totalProductCostWithoutMaintainingCost;
	}
	public double getLightAndMaintainCost() {
		return lightAndMaintainCost;
	}
	public void setLightAndMaintainCost(double lightAndMaintainCost) {
		this.lightAndMaintainCost = lightAndMaintainCost;
	}
	public double getTotalProductCost() {
		return totalProductCost;
	}
	public void setTotalProductCost(double totalProductCost) {
		this.totalProductCost = totalProductCost;
	}
	public double getProductSellingPrice() {
		return productLablingPrice;
	}
	public void setProductSellingPrice(double productLablingPrice) {
		this.productLablingPrice = productLablingPrice;
	}
	
	

	
	

}
